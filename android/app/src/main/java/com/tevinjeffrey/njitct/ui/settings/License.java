package com.tevinjeffrey.njitct.ui.settings;

public class License {
    String name;
    String author;
    String website;
    String type;
    String content;
    String usage;

    public String getName() {
        return name;
    }

    public String getAuthor() {
        return author;
    }

    public String getWebsite() {
        return website;
    }

    public String getType() {
        return type;
    }

    public String getContent() {
        return content;
    }

    public String getUsage() {
        return usage;
    }

}
